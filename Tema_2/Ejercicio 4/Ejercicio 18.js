const frase = "El lenguaje JavaScript es muy poderoso";
// Separa la frase en palabras usando el espacio como separador
const palabras = frase.split(" ");
// Cuenta cuántas palabras hay
const cantidadPalabras = palabras.length;
console.log(`La frase tiene ${cantidadPalabras} palabras.`); // La frase tiene 6 palabras.