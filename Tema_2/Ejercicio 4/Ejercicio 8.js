const texto = "Frontend Developer";
// Verifica si incluye "end"
const incluyeEnd = texto.includes("end");
console.log(`Incluye "end": ${incluyeEnd}`); // true
// Verifica si empieza con "Front"
const empiezaFront = texto.startsWith("Front");
console.log(`Empieza con "Front": ${empiezaFront}`); // true
// Verifica si termina con "per"
const terminaPer = texto.endsWith("per");
console.log(`Termina con "per": ${terminaPer}`); // true