// Guardar un número del 1 al 3 en una variable
let opcion = 2; // Puedes cambiarlo para probar otras opciones
// Usar switch para mostrar el mensaje correspondiente
switch (opcion) {
    case 1:
        console.log("Opción A seleccionada");
        break;
    case 2:
        console.log("Opción B seleccionada");
        break;
    case 3:
        console.log("Opción C seleccionada");
        break;
    default:
        console.log("Opción no válida");
}