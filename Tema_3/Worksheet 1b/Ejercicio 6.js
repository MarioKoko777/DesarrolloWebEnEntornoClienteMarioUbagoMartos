// Función arrow
const introduceYourself = (name, age) => `Hello, I am ${name}, and I am ${age} years old.`;
// Ejemplos de uso
console.log(introduceYourself("Alice", 25)); // "Hello, I am Alice, and I am 25 years old."
console.log(introduceYourself("Bob", 30));   // "Hello, I am Bob, and I am 30 years old."